                    <p>İzmir Büyükşehir Belediyesi kent genelinde sayıları giderek artan elektrikli otomobiller için şarj istasyonu altyapısını güçlendiriyor. İZELMAN bünyesindeki 14 otoparka toplam 24 istasyon kuruldu. Elektrikli araç sahipleri otoparklardan yüzde 50 indirimle faydalanıyor. İzmir Büyükşehir Belediye Başkanı Tunç Soyer’in 2050’de “sıfır karbon” hedefi doğrultusunda çalışan Büyükşehir Belediyesi, kentte elektrikli taşıtların yaygınlaşması için çalışmalarını sürdürüyor. İZELMAN A.Ş. bünyesindeki 14 açık ve kapalı otoparka toplam 24 elektrikli araç şarj istasyonu kuruldu.</p>
                    <p><b>Hedef yıl sonuna kadar tüm otoparklarda şarj istasyonu</b></p>
                    <p>İZELMAN A.Ş. Genel Müdürü Burak Alp Ersen, yıl sonuna kadar tüm otoparklarda şarj istasyonlarının yer almasının hedeflendiğini söyledi. Ersen, “2022 yılında İZELMAN A.Ş. bünyesindeki elektrikli araçların sayısını 74’den 100’e çıkarmayı hedefliyoruz. Bu araçların bir kısmını İzmir Büyükşehir Belediyesi hizmet aracı olarak kullanmayı, bir kısmını da MOOV uygulamasına dahil etmeyi planlıyoruz” dedi. İzmir Büyükşehir Belediyesi’nin elektrikli araçların kullanımını teşvik etmek amacıyla iş ortaklığı yaptığı araç paylaşım platformu MOOV uygulaması hakkında da bilgi veren Ersen, “Özellikle Avrupa ülkelerinde yaygın olarak kullanılan araç paylaşım sistemine 15 tam elektrikli aracı konuşlandırarak, İzmirli hemşehrilerimizin tam elektrikli araçları MOOV uygulaması üzerinden kullanabilmelerini sağladık. Bu sayede temel hedefimiz, kentimizde karbon salımı düşük olan araçların yaygınlaşmasını sağlamak. Avrupa Birliği’ne üye olan ülkelerde 2030 yılına kadar fosil yakıtlı araçlar yerine elektrikli araçların yaygınlaştırılması planlanmakta. Biz de aynı hedef doğrultusunda çalışmalarımızı sürdüreceğiz” şeklinde konuştu.</p>
